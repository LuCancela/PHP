        A distância percorrida equivale a duas viagens e meia entre Londres e Nova York;<br><br>
        A viagem começou no dia 13 de outubro de 2022 e durou 11 dias e 1 hora;<br><br>
        A ave pode ter perdido metade do seu peso corporal durante o voo, disse o organizador da Birdlife Tasmânia, Eric
        Woehler;<br><br>
        Para terem energia, os fuselos absorvem 25% do tecido que forma seu trato digestivo, fígado e rins;<br><br>
        Normalmente, os fuselos migram para a Nova Zelândia, mas o recordista fez uma curva acentuada e pousou no leste
        da Tasmânia;<br><br>
        Essa “virada errada” aumentou a “capacidade de voo” da espécie. Para Woehler, isso levanta a questão: outro
        fuselo poderia no futuro voar ainda mais longe sem parar?
